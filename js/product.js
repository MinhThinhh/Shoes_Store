const limit = 12;
$(document).ready(function () {
  const number = $(".number-item");
  const page = Math.ceil((allProduct.length - 1) / limit);
  const currentPage = 1;
  var location = 1;
  const product1 = $("#product1");
  function renderProduct(currentPage) {
    const skip = (currentPage - 1) * limit;
    let j = skip + limit;
    $(".card").remove();
    for (var i = skip; i < j; i++) {
      if (allProduct[i]) {
        product1.append(`<div data-value=${i} class="card" style="width: 270px; ">
            <img class="card-img-top" src=${allProduct[i].img} alt="Card image cap">
            <div class="card-body">
                    <h5 style="height: 80px;" class="card-title text-center">${allProduct[i].name}</h5>
                    <p class="card-text text-center">${allProduct[i].rating}</p>
                    <p class="text-center">${allProduct[i].Price}</p>
            </div>
            </div>`);
      }
    }
    console.log($(".card"));
  }
  renderProduct(currentPage);
  for (var i = 0; i < page; i++) {
    // number.append(`<div data-value=${i+1} href="" class="border skip-number">${i+1}</div>`)
    if (i === 0) {
      number.append(
        `<div data-value=${i + 1} href="" class="border skip-number selected">${
          i + 1
        }</div>`
      );
    } else {
      number.append(
        `<div data-value=${i + 1} href="" class="border skip-number">${
          i + 1
        }</div>`
      );
    }
  }
  $(".skip-number").click(function () {
    $(".skip-number").removeClass("selected");
    $(this).addClass("selected");
    renderProduct($(this).attr("data-value"));
    location = $(this).attr("data-value");
  });

  const prev = $(".prev");
  const next = $(".next");
  const skipNumber = $(".skip-number");
  next.click(function () {
    if (location != page) {
      $(".skip-number").removeClass("selected");
      $(skipNumber[location]).addClass("selected");
      renderProduct(location + 1);
      location = location + 1;
    }
  });
  prev.click(function () {
    if (location != 1) {
      location = location - 1;
      $(".skip-number").removeClass("selected");
      $(skipNumber[location - 1]).addClass("selected");
      renderProduct(location);
    }
  });

  $("#product1").on("click", ".card", function () {
    localStorage.setItem("img", allProduct[$(this).attr("data-value")].img);
    localStorage.setItem("name", allProduct[$(this).attr("data-value")].name);
    localStorage.setItem(
      "rating",
      allProduct[$(this).attr("data-value")].rating
    );
    localStorage.setItem("Price", allProduct[$(this).attr("data-value")].Price);
    localStorage.setItem(
      "description",
      allProduct[$(this).attr("data-value")].description
    );
    window.location.href = "./detailProduct.html";
  });
});
